function relu(x: number): number { return Math.max(0, x); }
function sigmoid(x: number): number { return 1 / (1 + Math.exp(-x)); }
function matMulBias(x: number[], W: number[][], b: number[]): number[] {
  return W.map((row, i) => { let s = b[i]; for (let j = 0; j < x.length; j++) s += row[j] * x[j]; return s; });
}

export interface TFJSDecoderResult {
  attention: number; workload: number; arousal: number;
  bandStats: ReturnType<typeof bandStats>;
  decoder: "tfjs-eeg-v1"; durationMs: number; confidence: number;
}

export function tfjsDecode(signal: EEGSignal): TFJSDecoderResult {
  const t0 = performance.now();
  const b = bandStats(signal);
  const x = [b.delta, b.theta, b.alpha, b.beta, b.gamma];
  const h1 = matMulBias(x, W1, B1).map(relu);
  const h2 = matMulBias(h1, W2, B2).map(relu);
  const out = matMulBias(h2, W3, B3).map(sigmoid);
  const sig = (v: number) => sigmoid(Math.log(Math.max(1e-9, v)));
  const blend = (t: number, s: number) => 0.7 * t + 0.3 * s;
  const attention = blend(out[0], sig(b.beta / Math.max(1e-9, b.alpha + b.theta)));
  const workload  = blend(out[1], sig(b.theta / Math.max(1e-9, b.alpha)));
  const arousal   = blend(out[2], Math.min(1, Math.max(0, b.beta + b.gamma)));
  const bands = [b.delta, b.theta, b.alpha, b.beta, b.gamma];
  const entropy = -bands.reduce((s, p) => s + (p > 0 ? p * Math.log(p) : 0), 0) / Math.log(5);
  return {
    attention: Math.min(1, Math.max(0, attention)),
    workload:  Math.min(1, Math.max(0, workload)),
    arousal:   Math.min(1, Math.max(0, arousal)),
    bandStats: b, decoder: "tfjs-eeg-v1",
    durationMs: +(performance.now() - t0).toFixed(2),
    confidence: Math.min(1, Math.max(0.5, entropy)),
  };
  }
